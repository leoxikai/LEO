tabby_cat="\tI'm tabbled in."
#\t ------- Horizontal Tab (TAB) ---横向制表符，也就是一个tab键
persian_cat="I'm split\non a line."
#\n -------- ASCII linefeed (LF)----换行
backslash_cat="I'm\\ a \\ cat."
#\\ -------- Backslash (\)----一个'\'

fat_cat="""
I'll do a list:
\t* Cat food 
\t* Fishies
\t* Catnip\n\t* Grass
"""
print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)

